=head1 NAME

Alt::CWB::ambs - A CPAN distribution of CWB

=head1 DESCRIPTION

The Open-CWB L<http://cwb.sourceforge.net/> project makes available
two modules to work with CWB version 3.0.x. Unfortunately they are not
available on CPAN, and therefore it is not possible to depend on them.

This B<ALTERNATE> distribution is exactly the same code as the one
prepared by Stefan Evert and company.

